/**
 * Student
 */
public class Students {

  private String name;
  private int no;

  public Students(String name, int no) {
    this.name = name;
    this.no = no;
  }


  public String getName() {
    return this.name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getNo() {
    return this.no;
  }

  public void setNo(int no) {
    this.no = no;
  }

}
